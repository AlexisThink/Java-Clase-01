
public class OperadoresAsignacion {

	public static void main(String[] args) 
	{
		//El operador de asignacion normal es el "="
		//+= Se suma asi mismo
		//-= Se resta asi mismo
		// /-=
		//*=
		//%=
		
		int numero=10;
		numero*=3;
		//numero=numero*3
		System.out.println(numero);
		
		
		numero+=3;
		System.out.println(numero);
		numero-=3;
		System.out.println(numero);
		numero/=3;
		System.out.println(numero);
		numero%=3;
		System.out.println(numero);
		
		
	}

}
